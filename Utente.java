public class Utente {
    private String nif;
    private String nome;
    private char genero;
    private String contacto;

    public Utente(String nif, String nome, char genero, String contacto) {
        this.nif = nif;
        this.nome = nome;
        this.genero = genero;
        this.contacto = contacto;
    }
}
